export function getProductGroups(): ProductGroup[] {
  const grouped: ProductGroup[] = [];
  const usedIds = new Set<string>();

  for (const [groupId, ids] of Object.entries(VARIANT_GROUPS)) {
    const variants = ids.map((id) => PRODUCTS.find((p) => p.id === id)).filter(Boolean) as Product[];
    if (variants.length === 0) continue;
    const first = variants[0];
    grouped.push({
      groupId,
      baseName: GROUP_BASE_NAMES[groupId] ?? first.name,
      category: first.category,
      image: first.image,
      description: first.description,
      cas: first.cas,
      molecularWeight: first.molecularWeight,
      blendComponents: first.blendComponents,
      variants,
    });
    ids.forEach((id) => usedIds.add(id));
  }

  for (const product of PRODUCTS) {
    if (!usedIds.has(product.id)) {
      grouped.push({
        groupId: product.id,
        baseName: product.name,
        category: product.category,
        image: product.image,
        description: product.description,
        cas: product.cas,
        molecularWeight: product.molecularWeight,
        blendComponents: product.blendComponents,
        variants: [product],
      });
    }
  }

  return grouped;
}

export function getGroupByProductId(productId: string): ProductGroup | undefined {
  const groups = getProductGroups();
  return groups.find((g) => g.variants.some((v) => v.id === productId));
}
